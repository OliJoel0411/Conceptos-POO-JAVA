public class Rectangulo {
    // Atributos
    private float lado1;
    private float lado2;

    public Rectangulo(float lado1, float lado2) {
        this.lado1 = lado1;
        this.lado2 = lado2;
    }
    // Metodo consultor
    public float getLado1() {
        return lado1;
    }
    // Metodo modificador
    public void setLado1(float lado1) {
        this.lado1 = lado1;
    }
    // Metodo consultor
    public float getLado2() {
        return lado2;
    }
    // Metodo modificador
    public void setLado2(float lado2) {
        this.lado2 = lado2;
    }
    
    // Metodo analizador
    
}
